# include <stdio.h>

// main
int main() { // or main(); "void" means the function has no argument
    printf("hello world\n"); // new line
    printf("Hello,\tWorld\n"); // tab
    printf("C:\\Program Files\n"); // backslash
    printf("It\'s a sunny day\n"); // single quote
    printf("He said, \"Hello\"\n"); // double quote
    printf("Hello\rWorld\n"); // Carriage Return: Moves the cursor to the beginning of the line
    printf("Hello\b"); // Backspace: Moves the cursor one character back.
}
// end main